# 01_variables_and_types.py

# Variables: containers to store data values

# Integer
age = 25
print("Age:", age)

# Float (decimal number)
height = 5.9
print("Height:", height)

# String (text)
name = "Brian"
print("Name:", name)

# Boolean (True or False)
is_student = True
print("Is student?", is_student)

# Basic operations
a = 10
b = 3

print("Addition:", a + b)          # 13
print("Subtraction:", a - b)       # 7
print("Multiplication:", a * b)    # 30
print("Division:", a / b)          # 3.3333...
print("Integer Division:", a // b) # 3
print("Modulus (remainder):", a % b) # 1
print("Exponentiation:", a ** b)  # 1000 (10 to the power 3)

